package jun24th;

public class IfElseDemo1 {

	public static void main(String[] args) {
		
		int x=10,y=20;
		
		if(x>y)
		{
			System.out.println(" x is bigger");
		}
		else if(x<y)
		{
			System.out.println(" x is smaller");
		}
		else if(x==y)
		{
			System.out.println("x is same as y");
		}

	}

}
